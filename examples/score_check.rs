use rapid_phonetic_matcher::PhoneticMatcher;

fn main() {
    let m = PhoneticMatcher::new();

    let pairs: Vec<(&str, &str, &str)> = vec![
        // 有声破裂音混同 (b/d/g)
        ("カバン", "カダン", "有声破裂音 b/d"),
        ("バグ", "ダグ", "有声破裂音 b/d"),
        ("ガム", "ダム", "有声破裂音 g/d"),
        ("ゴボウ", "ドボウ", "有声破裂音 g/d"),

        // 清濁ペア (k/g, t/d, s/z, h/b/p)
        ("カキ", "ガキ", "清濁 k/g"),
        ("タイ", "ダイ", "清濁 t/d"),
        ("サル", "ザル", "清濁 s/z"),
        ("ハン", "バン", "清濁 h/b"),
        ("バス", "パス", "清濁 b/p"),
        ("バレット", "パレット", "清濁 b/p"),

        // 鼻音 (m/n)
        ("カミ", "カニ", "鼻音 m/n"),
        ("マル", "ナル", "鼻音 m/n"),
        ("サンマ", "サンナ", "鼻音 m/n"),

        // 母音混同
        ("カラ", "キラ", "母音 a/i"),
        ("スシ", "ソシ", "母音 u/o"),
        ("テスト", "タスト", "母音 e/a"),

        // 長音の有無
        ("コーヒー", "コヒ", "長音省略"),
        ("ビール", "ビル", "長音省略"),
        ("メール", "メル", "長音省略"),
        ("カレー", "カレ", "長音省略"),
        ("センター", "センタ", "長音省略"),

        // 促音の有無
        ("サッカー", "サカ", "促音+長音省略"),
        ("マッチ", "マチ", "促音省略"),
        ("ロケット", "ロケト", "促音省略"),

        // 拗音・合拗音混同
        ("シャチ", "サチ", "sh/s混同"),
        ("チャンス", "タンス", "ch/t混同"),
        ("ジュース", "ズース", "j/z混同"),

        // f/h混同
        ("ファイル", "ハイル", "f/h混同"),
        ("フィルム", "ヒルム", "f/h混同"),

        // 完全に異なる語
        ("リンゴ", "バナナ", "完全に異なる"),
        ("サクラ", "ヒマワリ", "完全に異なる"),
        ("テレビ", "ラジオ", "完全に異なる"),

        // 同一語
        ("コンピューター", "コンピューター", "同一語"),
        ("インターネット", "インターネット", "同一語"),

        // 複合変化
        ("タマゴ", "タマコ", "清濁 g/k"),
        ("カガミ", "カカミ", "清濁 g/k"),
        ("ミカヅキ", "ミガズキ", "清濁 複数"),
    ];

    println!("{:<20} {:<20} {:<25} {:>6}", "入力", "候補", "カテゴリ", "スコア");
    println!("{}", "-".repeat(75));
    for (a, b, category) in &pairs {
        let score = m.calculate_similarity(a, b);
        println!("{:<20} {:<20} {:<25} {:>6.3}", a, b, category, score);
    }
}