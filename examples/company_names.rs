use rapid_phonetic_matcher::PhoneticMatcher;

fn main() {
    let m = PhoneticMatcher::new();

    // 会社名マスタ (カタカナ読み)
    let companies = vec![
        "カルビー", "カルディ", "キリン", "サントリー", "アサヒ",
        "ソニー", "パナソニック", "トヨタ", "ホンダ", "ニッサン",
        "マイクロソフト", "グーグル", "アマゾン", "アップル", "メタ",
        "スターバックス", "マクドナルド", "ファミリーマート", "セブンイレブン", "ローソン",
        "ヤマダデンキ", "ビックカメラ", "ヨドバシカメラ", "ユニクロ", "ニトリ",
        "ソフトバンク", "ドコモ", "エーユー",
        "シャープ", "ダイキン", "ブリヂストン",
    ];

    // STT誤変換の想定パターン (入力, 期待される正解会社名, 誤変換の説明)
    let test_cases: Vec<(&str, &str, &str)> = vec![
        // 有声破裂音混同
        ("カルミー", "カルビー", "m→b: 「軽美」的な誤変換"),
        ("カルディー", "カルディ", "長音付加の揺れ"),
        ("カルビ", "カルビー", "長音省略"),

        // 清濁混同
        ("ドヨダ", "トヨタ", "t/d混同: 豊田→ドヨダ"),
        ("ホンタ", "ホンダ", "d/t混同: 本田→ホンタ"),
        ("ニッザン", "ニッサン", "s/z混同: 日産"),
        ("バナソニック", "パナソニック", "p/b混同"),
        ("ソフトバンク", "ソフトバンク", "完全一致"),
        ("ゾフトバンク", "ソフトバンク", "s/z混同"),
        ("ドコモ", "ドコモ", "完全一致"),
        ("トコモ", "ドコモ", "d/t混同"),

        // f/h混同
        ("ハミリーマート", "ファミリーマート", "f/h混同"),

        // 長音省略・付加
        ("ソニ", "ソニー", "長音省略"),
        ("キリン", "キリン", "完全一致"),
        ("サントリ", "サントリー", "長音省略"),

        // 拗音・合拗音
        ("サープ", "シャープ", "sh/s混同"),

        // 複合
        ("クークル", "グーグル", "g/k混同"),
        ("アマソン", "アマゾン", "z/s混同"),
        ("マクトナルド", "マクドナルド", "d/t混同"),
        ("スタアバクス", "スターバックス", "長音展開+促音省略"),
        ("ダイギン", "ダイキン", "k/g混同"),
        ("ブリジストン", "ブリヂストン", "ヂ/ジの揺れ"),
        ("ユニグロ", "ユニクロ", "k/g混同"),
        ("ニドリ", "ニトリ", "t/d混同"),

        // 鼻音混同
        ("ヤナダデンキ", "ヤマダデンキ", "m/n混同"),
        ("ビクカネラ", "ビックカメラ", "m/n混同+促音省略"),

        // 全く関係ない入力
        ("ラーメン", "?", "関係ない語"),
    ];

    println!("=== 会社名マッチングテスト ===\n");
    println!("{:<20} {:<20} {:<20} {:>6}  {}", "STT入力", "期待会社名", "Top1結果", "スコア", "説明");
    println!("{}", "=".repeat(95));

    for (input, expected, desc) in &test_cases {
        let results = m.find_top_matches(input, &companies, 3);
        let top = &results[0];
        let correct = if *expected == "?" {
            "N/A".to_string()
        } else if top.text == *expected {
            "✓".to_string()
        } else {
            format!("✗ (expected {})", expected)
        };
        println!(
            "{:<20} {:<20} {:<20} {:>6.3}  {} [{}]",
            input, expected, top.text, top.score, desc, correct
        );
        if results.len() > 1 {
            println!("  2nd: {:<16} {:>6.3}", results[1].text, results[1].score);
            if results.len() > 2 {
                println!("  3rd: {:<16} {:>6.3}", results[2].text, results[2].score);
            }
        }
    }
}